import random

def float_to_string(value):
    return str(value)

# Generate three random floats
a = random.random()
b = random.random()
c = random.random()

# Convert floats to strings
str_a = float_to_string(a)
str_b = float_to_string(b)
str_c = float_to_string(c)

# Print the results (optional)
print(str_a, str_b, str_c)