def main():
    # Declare a variable called totalSales
    totalSales = 0

    # Add sales for each month to the totalSales variable
    january_sales = 1200
    february_sales = 1500
    march_sales = 1800

    totalSales += january_sales
    totalSales += february_sales
    totalSales += march_sales

    # Print the total sales to the screen
    print("Total Sales:", totalSales)

if __name__ == "__main__":
    main()
